package com.lemzeeyyy.movieapp.request;

import com.lemzeeyyy.movieapp.model.MovieModel;
import com.lemzeeyyy.movieapp.response.MovieSearchResponse;

import retrofit2.Call;
import retrofit2.http.GET;
import retrofit2.http.Path;
import retrofit2.http.Query;

public interface MovieApi {
    //Search for movies
    //https://api.themoviedb.org/3/search/movie?api_key={api_key}&query=Jack+Reacher
    @GET("/3/search/movie")
    Call<MovieSearchResponse> searchMovie(
            @Query("api_key") String key,
            @Query("query") String query,
            @Query("page") int page
    );

    // Making Search with ID
    // https://api.themoviedb.org/3/movie/550?api_key=xxxxxxxxxxxxx
    // Remember that movie_id= 550 is for Fight Club movie


    @GET("3/movie/{movie_id}?")
    Call<MovieModel> getMovie(
            @Path("movie_id") int movie_id,
            @Query("api_key") String api_key
    );
}
